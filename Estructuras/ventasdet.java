package Estructuras;

/**
 *
 * @author carda
 */
public class ventasdet {
    int noFacHijo;
    int idArtHijo;
    float cant;
    float preUnit;
    float monto;
    
    String des;

    public int getNoFacHijo() {
        return noFacHijo;
    }

    public void setNoFacHijo(int noFacHijo) {
        this.noFacHijo = noFacHijo;
    }

    public int getIdArtHijo() {
        return idArtHijo;
    }

    public void setIdArtHijo(int idArtHijo) {
        this.idArtHijo = idArtHijo;
    }

    public float getCant() {
        return cant;
    }

    public void setCant(float cant) {
        this.cant = cant;
    }

    public float getPreUnit() {
        return preUnit;
    }

    public void setPreUnit(float preUnit) {
        this.preUnit = preUnit;
    }

    public float getMonto() {
        return monto;
    }

    public void setMonto(float monto) {
        this.monto = monto;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }
}
