    public void rArt(){
        if(txtId.getText().equals("0")){
            return;
        }
        String qr;
        Object[] vl;
        qr = "SELECT * FROM articulos WHERE idArtPadre=?";
        vl = new Object[]{txtId.getText()};
        BeanListHandler blh = new BeanListHandler(articulos.class);
        List<articulos> lArt = ejSQL.select(qr, blh, vl);
        
        if(lArt.isEmpty()){
            mp.txtEnabled(txtId);
            return;
        }
        
        lArt.forEach(item->{
            txtCod.setText(item.getCod());
            txtDes.setText(item.getDes());
            txtPreVenta.setText(String.valueOf(item.getPreVenta()));
            txtPreCosto.setText(String.valueOf(item.getPreCosto()));
            if (item.isEstado()){
                chbEstado.setSelected(true);
            } else {
                chbEstado.setSelected(false);
            }
        });
        
        mp.txtDisabled(txtId);
    }
