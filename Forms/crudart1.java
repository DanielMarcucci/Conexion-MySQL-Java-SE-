    public boolean validarBtn(){
        if(txtCod.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Es necesario ingresar un código");
            txtCod.requestFocus();
            return false;
        } else if(txtId.getText().equals("0")){
            JOptionPane.showMessageDialog(null, "Es necesario ingresar un ID que no sea '0'");
            txtId.requestFocus();
            return false;
        }
        return true;
    }
