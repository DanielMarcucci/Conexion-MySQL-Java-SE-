    private void bArtSele(JTable tabla) {
        try {
            int row = tabla.getSelectedRow();
            if (row < 0) {
                row = 0;
            }
            txtId.setText(String.valueOf(listaArt.get(row).getIdArtPadre()));
            txtCod.setText(listaArt.get(row).getCod());
            txtDes.setText(listaArt.get(row).getDes());
            txtPreVenta.setText(String.valueOf(listaArt.get(row).getPreVenta()));
            txtPreCosto.setText(String.valueOf(listaArt.get(row).getPreCosto()));
            if (listaArt.get(row).isEstado()){
                chbEstado.setSelected(true);
            } else {
                chbEstado.setSelected(false);
            }
            mp.txtDisabled(txtId);
        } catch (Exception ex) {
            showMessageDialog(null,"Error: "+ex);
        }
    }
